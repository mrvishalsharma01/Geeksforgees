class Solution {
  public:
    int countSubarrays(vector<int> &arr, int k) {
        // code here
         unordered_map<int,int> mpp;
        int pre = 0;
        int ans = 0;
        for(auto it:arr){
            pre+=it;
            int req = pre-k;
            if(pre==k)ans++;
            if(mpp.find(req)!=mpp.end()){
                ans+=mpp[req];
            }
            mpp[pre]++;
        }
        return ans;
    }
};

